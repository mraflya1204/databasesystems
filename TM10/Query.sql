LIMIT 5;

SELECT
	EXTRACT(MONTH FROM N_TGLMASUK) AS bulan,
    EXTRACT(YEAR FROM N_TGLMASUK) AS tahun,
    SUM(ND_BANYAK * JL_HARGA) AS harga
FROM NOTA 
JOIN NOTA_DETAIL ON NOTA.N_NO = NOTA_DETAIL.NOTA_N_NO
JOIN JENIS_LAYANAN ON JENIS_LAYANAN.JL_ID = NOTA_DETAIL.JENIS_LAYANAN_JL_ID
WHERE EXTRACT(YEAR FROM N_TGLMASUK) IS NOT NULL
GROUP BY bulan, tahun
ORDER BY bulan ASC

SELECT 
	P_NO AS no_pelanggan,
    P_NAMA AS nama,
    SUM(ND_BANYAK * JL_HARGA) AS Total_Payment
FROM PELANGGAN 
JOIN NOTA N ON P_NO = N.PELANGGAN_P_NO
JOIN NOTA_DETAIL ND ON N_NO = ND.NOTA_N_NO
JOIN JENIS_LAYANAN JL ON ND.JENIS_LAYANAN_JL_ID = JL_ID
WHERE EXTRACT(MONTH FROM N_TGLMASUK) = 7 AND EXTRACT(YEAR FROM N.N_TGLMASUK) = 2010
GROUP BY P_NAMA, P_NO
ORDER BY Total_Payment DESC
LIMIT 1;

