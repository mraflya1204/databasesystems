CREATE TABLE PELANGGAN (  
P_NO            CHAR(5),  
P_NAMA          VARCHAR(60),  
P_JENISKEL      CHAR(1),  
P_TEMPATLAHIR   VARCHAR(30),  
P_TGLLAHIR      DATE,  
P_ALAMAT        VARCHAR(200),  
P_NOTELP        VARCHAR(15),  
P_TGLDAFTAR     DATE,  
PRIMARY KEY (P_NO)  
);

CREATE TABLE NOTA (    
N_NO            INT,    
PRIMARY KEY (N_NO),   
N_TGLMASUK      DATE,    
N_TGLSELESAI    DATE,    
N_TGLJADI       DATE,    
N_TGLAMBIL      TIMESTAMP,    
N_STATUS        VARCHAR(10),    
N_KODERAK       VARCHAR(3),    
PELANGGAN_P_NO  CHAR(5),   
FOREIGN KEY (PELANGGAN_P_NO) REFERENCES PELANGGAN (P_NO)    
);

CREATE TABLE JENIS_LAYANAN (   
JL_ID           VARCHAR(5) NOT NULL,   
PRIMARY KEY (JL_ID),   
JL_NAMA         VARCHAR(100),   
JL_HARGA        DECIMAL(10,2)   
);

CREATE TABLE NOTA_DETAIL (   
ND_NO           INT PRIMARY KEY,   
NOTA_N_NO       INT,   
JENIS_LAYANAN_JL_ID VARCHAR(5),   
ND_BANYAK     INT,  
FOREIGN KEY (NOTA_N_NO) REFERENCES NOTA(N_NO),   
FOREIGN KEY (JENIS_LAYANAN_JL_ID) REFERENCES JENIS_LAYANAN(JL_ID)   
);

CREATE TABLE KARYAWAN (  
K_NO    CHAR(2) PRIMARY KEY,  
K_NAMA  VARCHAR(100),  
K_BAGIAN    VARCHAR(25),  
K_TGLMASUK  DATE,  
K_EMAIL     VARCHAR(100),  
K_PASSWORD  VARCHAR(20),  
SPV_K_NO    CHAR(2)  
);

ALTER TABLE KARYAWAN ADD  
FOREIGN KEY (SPV_K_NO) REFERENCES KARYAWAN(K_NO);

CREATE TABLE KARYAWAN_DETAIL (  
KD_NO       INT PRIMARY KEY,  
KARYAWAN_K_NO CHAR(2),  
NOTA_N_NO   INT,  
FOREIGN KEY (KARYAWAN_K_NO) REFERENCES KARYAWAN(K_NO),  
FOREIGN KEY (NOTA_N_NO) REFERENCES NOTA(N_NO)  
);

