    FROM pelanggan
    INNER JOIN nota ON pelanggan.p_no = nota.pelanggan_p_no
    INNER JOIN nota_detail ON nota.n_no = nota_detail.nota_n_no
    INNER JOIN jenis_layanan ON nota_detail.jenis_layanan_jl_id = jenis_layanan.jl_id
    WHERE jl_nama = 'setrika'
)
ORDER BY p_no;
